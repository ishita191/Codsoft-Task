    private void loadStudentsFromFile() {
    try {
        java.io.BufferedReader br = new java.io.BufferedReader(new java.io.FileReader("students.txt"));
        String line;

        while((line = br.readLine()) != null) {

            String[] data = line.split(",");

            String name = data[0];
            int roll = Integer.parseInt(data[1]);
            String grade = data[2];

            Student s = new Student(name, roll, grade);
            students.add(s);

            model.addRow(new Object[]{name, roll, grade});
        }

        br.close();

    } catch(Exception e) {
        System.out.println("No file found yet");
    }
}

